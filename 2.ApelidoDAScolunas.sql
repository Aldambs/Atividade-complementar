/*
Listar o código, o nome e a data de nascimento
de todos os jogadores. Os apelidado para as
colunas devem ser código, nome e data,
respectivamente.
*/
SELECT COD_JOG AS  [CÓDIGO DO JOGADOR]
	,nom_jog AS NOME
	,dat_nasc AS [DATA DE NASCIMENTO]
	,cod_pos AS [CÓDIGO DA POSIÇÃO]
	,salario AS SALÁRIO
FROM JOGADORES